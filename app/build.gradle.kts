// Load Ollama API URL from secrets.properties
val secretsFile = rootProject.file("secrets.properties")
val ollamaApiUrl = if (secretsFile.exists()) {
    val content = secretsFile.readText()
    val match = Regex("OLLAMA_API_URL=(.*)").find(content)
    match?.groupValues?.get(1) ?: "http://localhost:11434"
} else {
    "http://localhost:11434"
}

